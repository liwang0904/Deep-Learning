from numpy import array
from numpy.linalg import qr

A = array([
	[1, 2],
	[3, 4],
	[5, 6]])
# [[1 2]
#  [3 4]
#  [5 6]]
print(A)

Q, R = qr(A, 'complete')
# [[-0.16903085  0.89708523  0.40824829]
#  [-0.50709255  0.27602622 -0.81649658]
#  [-0.84515425 -0.34503278  0.40824829]]
print(Q)
# [[-5.91607978 -7.43735744]
#  [ 0.          0.82807867]
#  [ 0.          0.        ]]
print(R)

B = Q.dot(R)
# [[1. 2.]
#  [3. 4.]
#  [5. 6.]]
print(B)
